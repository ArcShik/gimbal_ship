/**
  * @brief This function handles USART3 global interrupt.
  */
void USART3_IRQHandler(void)
{
  /* USER CODE BEGIN USART3_IRQn 0 */
    if(huart3.Instance->SR & UART_FLAG_RXNE)
    {
        __HAL_UART_CLEAR_PEFLAG(&huart3);
    }
    else if(USART3->SR & UART_FLAG_IDLE)
    {
        static uint16_t this_time_rx_len = 0;

        __HAL_UART_CLEAR_PEFLAG(&huart3);

        if ((hdma_usart3_rx.Instance->CR & DMA_SxCR_CT) == RESET)
        {
            /* Current memory buffer used is Memory 0 */
            //Â§±ÊïàDMA
            __HAL_DMA_DISABLE(&hdma_usart3_rx);

            //get receive data length, length = set_data_length - remain_length
            //Ëé∑ÂèñÊé•Êî∂Êï∞ÊçÆÈïøÂ∫¶,ÈïøÂ∫¶ = ËÆæÂÆöÈïøÂ∫¶ - Ââ©‰ΩôÈïøÂ∫¶
            this_time_rx_len = SBUS_RX_BUF_NUM - hdma_usart3_rx.Instance->NDTR;

            //ÈáçÊñ∞ËÆæÂÆöÊï∞ÊçÆÈïøÂ∫¶
            hdma_usart3_rx.Instance->NDTR = SBUS_RX_BUF_NUM;

            //ËÆæÂÆöÁºìÂÜ≤Ôø??1
            hdma_usart3_rx.Instance->CR |= DMA_SxCR_CT;

            //‰ΩøËÉΩDMA
            __HAL_DMA_ENABLE(&hdma_usart3_rx);

            if(this_time_rx_len == SBUS_FRAME_SIZE)
            {
                //Â§ÑÁêÜÈÅ•ÊéßÂô®Êï∞Ôø??
                sbus_rc_decode(sbus_rx_buf[0]);
//                rt_timer_start(rc_timer);
            }
        }
        else
        {
            /* Current memory buffer used is Memory 1 */
            //Â§±ÊïàDMA
            __HAL_DMA_DISABLE(&hdma_usart3_rx);

            //get receive data length, length = set_data_length - remain_length
            //Ëé∑ÂèñÊé•Êî∂Êï∞ÊçÆÈïøÂ∫¶,ÈïøÂ∫¶ = ËÆæÂÆöÈïøÂ∫¶ - Ââ©‰ΩôÈïøÂ∫¶
            this_time_rx_len = SBUS_RX_BUF_NUM - hdma_usart3_rx.Instance->NDTR;

            //ÈáçÊñ∞ËÆæÂÆöÊï∞ÊçÆÈïøÂ∫¶
            hdma_usart3_rx.Instance->NDTR = SBUS_RX_BUF_NUM;

            //ËÆæÂÆöÁºìÂÜ≤Ôø??0
            DMA1_Stream1->CR &= ~(DMA_SxCR_CT);

            //‰ΩøËÉΩDMA
            __HAL_DMA_ENABLE(&hdma_usart3_rx);

            if(this_time_rx_len == SBUS_FRAME_SIZE)
            {
                //Â§ÑÁêÜÈÅ•ÊéßÂô®Êï∞Ôø??
                sbus_rc_decode(sbus_rx_buf[1]);
//                rt_timer_start(rc_timer);
            }
        }
    }
  /* USER CODE END USART3_IRQn 0 */
  HAL_UART_IRQHandler(&huart3);
  /* USER CODE BEGIN USART3_IRQn 1 */

  /* USER CODE END USART3_IRQn 1 */
}

